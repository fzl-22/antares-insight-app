/// Types for [SnackBar]
///
/// 1. [SnackBarType.success], used when calling [CoreUtils.showSnackBar]
///    for success-type message.
/// 2. [SnackBarType.warning], for warning-type message.
/// 3. [SnackBarType.error], for error-type message.
enum SnackBarType {
  success,
  warning,
  error,
}
